    private void tbCustMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCustMouseClicked
        int bar = tbCust.getSelectedRow();
        String a = tabmode.getValueAt(bar,1).toString();
        String b = tabmode.getValueAt(bar,2).toString();
        String c = tabmode.getValueAt(bar,3).toString();
        
        try {
            String sql = "SELECT id_cust FROM customer where nama_cust = '"+a+"' and alamat_cust ='"+b+"' and telp_cust='"+c+"'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                ID = hasil.getString("id_cust");
                tNama.setText(a);
                tTelp.setText(c);
                tAlamat.setText(b);
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }

        
    }//GEN-LAST:event_tbCustMouseClicked

