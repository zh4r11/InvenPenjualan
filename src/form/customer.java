    protected void nourut(){
        int brs = tabmode.getRowCount();
        for(int i=0; i<brs; i++){
            String no = String.valueOf(i+1);
            tabmode.setValueAt(no+".",i,0);
        }
    }
    
    protected void datatable(){
        Object[] Baris ={"No.","Nama","Alamat","Telp"};
        tabmode = new DefaultTableModel(null, Baris);
        tbCust.setModel(tabmode);
        String cariitem = tCari.getText();

        try {
            String sql = "SELECT * FROM customer where id_cust like '%"+cariitem+"%' or nama_cust like'%"+cariitem+"%'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            nourut();
            while (hasil.next()){
                String nomor = hasil.getString("id_cust");
                String nama = hasil.getString("nama_cust");
                String telp = hasil.getString("alamat_cust");
                String alamat = hasil.getString("telp_cust");
                String[] data = {"",nama,telp,alamat};
                tabmode.addRow(data);
                nourut();
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
            }
    }

